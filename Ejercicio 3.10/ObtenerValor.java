public class ObtenerValor {
    public static void main(String[] args) {
        System.out.println("a) " + (7 / 2));
        System.out.println("b) " + (7 % 2));
        System.out.println("c) " + (12 / 3));
        System.out.println("d) " + (12 % 3));
        System.out.println("e) " + (0 % 5));
        System.out.println("f) " + (15 % 5));
        System.out.println("g) " + (7 * 10 - 50 % 3 * 4 + 9));
        System.out.println("h) " + ((7 * (10 - 5) % 3) * 4 + 9));
    }
}
